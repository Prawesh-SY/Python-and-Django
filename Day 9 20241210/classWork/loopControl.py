# break control statement
for i in range(10):
    print(i)
    if i==4:
        break
print("\n")
print("\n")
#continue control statement
for j in range(10):
    if j%2==0:
        continue
    print(j)
print("\n")
print("\n")

#pass control statement
for k in range(10):
    pass    #Used as a placeholder